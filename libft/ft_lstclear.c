#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;
	t_list	*new;

	if (!lst)
		return ;
	if (*lst == NULL)
		return ;
	temp = *lst;
	while (temp != NULL)
	{
		new = temp->next;
		ft_lstdelone(temp, del);
		temp = new;
	}
	*lst = NULL;
	return ;
}
