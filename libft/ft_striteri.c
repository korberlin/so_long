#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	len;

	if (!s)
		return ;
	len = 0;
	while (s[len])
	{
		f(len, &s[len]);
		len++;
	}
	return ;
}
